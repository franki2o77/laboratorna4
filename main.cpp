#include <iostream>
#include <cmath> // для sqrt
using namespace std;

int main() {
    // Виведення інформації про програму
    cout << "================================================\n";
    cout << "Автор програми: студент:Кулясоов Ілля групи 355 \n";
    cout << "Програма для розв'язання квадратного рівняння\n";
    cout << "Формат рівняння: ax^2 + bx + c = 0\n";
    cout << "================================================\n";

    // Введення коефіцієнтів
    float a, b, c; 
    cout << "Введіть коефіцієнт a: "; cin >> a;
    cout << "Введіть коефіцієнт b: "; cin >> b;
    cout << "Введіть коефіцієнт c: "; cin >> c;

    // Перевірка, чи є рівняння квадратним або лінійним
    if (a == 0) { // Лінійне рівняння
        if (b == 0) {
            if (c == 0) {
                cout << "Рівняння має безліч розв'язків (0 = 0).\n";
            } else {
                cout << "Рівняння не має розв'язків (c != 0).\n";
            }
        } else {
            cout << "Розв'язок лінійного рівняння: x = " << -c / b << '\n';
        }
    } else { // Квадратне рівняння
        float D = b * b - 4 * a * c; // Обчислення дискримінанта
        cout << "Дискримінант D = " << D << '\n';
        if (D < 0) {
            cout << "Рівняння не має дійсних розв'язків.\n";
        } else if (D == 0) {
            float x = -b / (2 * a);
            cout << "Рівняння має один розв'язок: x = " << x << '\n';
        } else {
            float x1 = (-b + sqrt(D)) / (2 * a);
            float x2 = (-b - sqrt(D)) / (2 * a);
            cout << "Рівняння має два розв'язки:\n";
            cout << "x1 = " << x1 << '\n';
            cout << "x2 = " << x2 << '\n';
        }
    }

    return 0;
}